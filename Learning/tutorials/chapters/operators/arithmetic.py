#arithmetic operators

x = input("enter value for x:")
y = input("enter value for y:")

#add
s = int(x) + int(y)
print("sum is ", s)

#sub
s = int(x) - int(y)
print("sub is ", s)

#mul
s = int(x) * int(y)
print("mul is ", s)

#div
s = int(x) / int(y)
print("div is ", s)

#floor div
# Floor division - division that results into whole number adjusted to the left in the number line
s = int(x) // int(y)
print("floor div is ", s)

#mod
s = int(x) % int(y)
print("mod is ", s)

